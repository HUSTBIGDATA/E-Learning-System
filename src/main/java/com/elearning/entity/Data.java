package com.elearning.entity;
	
public class Data {
	private int dataID;
	private String dataName;
	private String dataImage;
	private String dataPath;
	private String dataAbstract;
	private String dataType;
	private int dataDownloadSum;
	
	public int getDataID() {
		return dataID;
	}
	public void setDataID(int dataID) {
		this.dataID = dataID;
	}
	public String getDataName() {
		return dataName;
	}
	public void setDataName(String dataName) {
		this.dataName = dataName;
	}
	public String getDataImage() {
		return dataImage;
	}
	public void setDataImage(String dataImage) {
		this.dataImage = dataImage;
	}
	public String getDataPath() {
		return dataPath;
	}
	public void setDataPath(String dataPath) {
		this.dataPath = dataPath;
	}
	public String getDataAbstract() {
		return dataAbstract;
	}
	public void setDataAbstract(String dataAbstract) {
		this.dataAbstract = dataAbstract;
	}
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}
	public int getDataDownloadSum() {
		return dataDownloadSum;
	}
	public void setDataDownloadSum(int dataDownloadSum) {
		this.dataDownloadSum = dataDownloadSum;
	}
}
